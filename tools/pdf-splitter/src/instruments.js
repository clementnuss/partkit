/**
 * Get a normalized version of an instrument name for comparison
 */
export function normalizeInstrumentName(name) {
  return name
    .toLowerCase()
    .trim()
    .replace(/\s+/g, ' ')
    // Normalize unicode musical symbols
    .replace(/♭/g, 'b')
    .replace(/♯/g, '#');
}
